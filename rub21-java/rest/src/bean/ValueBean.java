public class ValueBean {
    Double x;
    Double y;
    Float weight;
    Integer problem_id;
    Double way_id;
    Double node_id;
    Float highwaykey;
    Float distance;
    String iso_a2;

    public Double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }

    public Float getWeight() {
        return weight;
    }

    public void setWeight(Float weight) {
        this.weight = weight;
    }

    public Integer getProblem_id() {
        return problem_id;
    }

    public void setProblem_id(Integer problem_id) {
        this.problem_id = problem_id;
    }

    public Double getWay_id() {
        return way_id;
    }

    public void setWay_id(Double way_id) {
        this.way_id = way_id;
    }

    public Double getNode_id() {
        return node_id;
    }

    public void setNode_id(Double node_id) {
        this.node_id = node_id;
    }

    public Float getHighwaykey() {
        return highwaykey;
    }

    public void setHighwaykey(Float highwaykey) {
        this.highwaykey = highwaykey;
    }

    public Float getDistance() {
        return distance;
    }

    public void setDistance(Float distance) {
        this.distance = distance;
    }

    public String getIso_a2() {
        return iso_a2;
    }

    public void setIso_a2(String iso_a2) {
        this.iso_a2 = iso_a2;
    }

}
